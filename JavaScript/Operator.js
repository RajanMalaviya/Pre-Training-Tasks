/**This file contains the basic operators in JavaScript.*/

let x = 5;
let y = 2;
let z = x + y;
console.log("x= "+x); // 5
console.log("y= "+y); // 2
console.log("z= "+z); // 7
console.log("x-y= "+(x-y)); // 5-2=3
console.log("x*y= "+x*y); // 5*2=10
console.log("x/y= "+x/y); // 5/2=2.5
console.log("x%y= "+x%y); // 5%2=1
console.log("x**y= "+x**y); // 5^2=25
console.log("x++= "+x++); // 5
console.log("++x= "+ ++x); // 7
console.log("x--= "+x--); // 7
console.log("--x= "+ --x); // 5
console.log("x+=y= "+(x+=y)); // 5+2=7
console.log("x-=y= "+(x-=y)); // 7-2=5
console.log("x*=y= "+(x*=y)); // 5*2=10
console.log("x/=y= "+(x/=y)); // 10/2=5
console.log("x%=y= "+(x%=y)); // 5%2=1
console.log("x**=y= "+(x**=y)); // 5^2=25
console.log("x="+(x)); // 25
console.log("y="+(y)); // 2
console.log("x==y= "+(x==y)); // false
console.log("x!=y= "+(x!=y)); // true
console.log("x>y= "+(x>y)); // true
console.log("x<y= "+(x<y)); // false
console.log("x>=y= "+(x>=y)); // true
console.log("x<=y= "+(x<=y)); // false
console.log("x&&y= "+(x&&y)); // 2
console.log("x||y= "+(x||y)); // 5
